	<h2>About / History</h2><a name="about" ></a>
	<p>WW2 game started in August 2005 as a simple ww2 themed game based upon a well known game of that time.
	After a few months of development, the first public release was in January 2006. World War 2 thrived for several
	months, then slowly started to decline in activity. Many changes were added between 2006 and 2009, including
	one overall theme change in 2008. In May 2009, the whole game was rewritten from the ground up with a new interface,
	and many behind the scenes improvements.</p>
	<p>Because WW2 game was originally based upon a text based game, it continues to be 100% text based, with no
	interactable graphics. The advantages of this are mainly for compatibility with older computers and phones that do not
	have the hardware capable of running modern graphics.</p>
	<p>The game play itself is centered around the player, whom is of one of five nations, building up her or her army,
	and attacking other players to increase their overall rank.</p>
